namespace Calculator_WebApi.Core
{
    public class CalculatorLogic
    {
        public int Summ (int x, int y)
        {
            return x + y;
        }


        public int Difference(int x, int y)
        {
            return x - y;
        }

        public int Multipli(int x, int y)
        {
            return x * y;
        }

        public int Division(int x, int y)
        {
            return x / y;
        }
    }
}
